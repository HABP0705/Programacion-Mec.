#include <stdio.h>
#include <stdlib.h>
#include <ctype.h>

/* Verifica.
   El programa, al recibir como datos una cadena de caracteres y una posición específica en la cadena,
   determina si el caracter correspondiente es una letra minúscula. */

void main(void)
{
    char p, cad[50];
    int n;

    printf("\nIngrese la cadena de caracteres (maximo 50): ");
    gets(cad); // Lee la cadena de caracteres

    printf("\nIngrese la posicion en la cadena que desea verificar: ");
    scanf("%d", &n); // Lee la posición a verificar

    if ((n > 0) && (n <= 50))
    {
        p = cad[n - 1]; // Ajusta la posición (el usuario ingresa 1-based index)

        if (islower(p)) // Verifica si el caracter es minúscula
            printf("\n%c es una letra minuscula", p);
        else
            printf("\n%c no es una letra minuscula", p);
    }
    else
    {
        printf("\nEl valor ingresado de n es incorrecto");
    }
}
